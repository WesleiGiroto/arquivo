SELECT 
    NOME_MECANICO,
    COUNT(CASE WHEN P.ITEM = 'M0041' THEN 1 END) AS "1ª",
    COUNT(CASE WHEN P.ITEM = 'M0051' THEN 1 END) AS "2ª",
    COUNT(CASE WHEN P.ITEM = 'M0061' THEN 1 END) AS "3ª",
    COUNT(CASE WHEN P.ITEM = 'M0071' THEN 1 END) AS "4ª",
    COUNT(CASE WHEN P.ITEM = 'M0081' THEN 1 END) AS "5ª",
    COUNT(CASE WHEN P.ITEM = 'M0091' THEN 1 END) AS "6ª",
    COUNT(CASE WHEN P.ITEM = 'M0101' THEN 1 END) AS "7ª",
    COUNT(CASE WHEN P.ITEM = 'M0111' THEN 1 END) AS "8ª",
    COUNT(CASE WHEN P.ITEM = 'M0121' THEN 1 END) AS "9ª",
    COUNT(CASE WHEN P.ITEM = 'M0131' THEN 1 END) AS "10ª",
    COUNT(CASE WHEN P.ITEM = 'M0141' THEN 1 END) AS "11ª",
    COUNT(CASE WHEN P.ITEM = 'M0151' THEN 1 END) AS "12ª",
    COUNT(CASE WHEN P.ITEM = 'M0161' THEN 1 END) AS "13ª",
    COUNT(CASE WHEN P.ITEM = 'M0171' THEN 1 END) AS "14ª",
    COUNT(CASE WHEN P.ITEM IN (
        'M0041','M0051','M0061','M0071','M0081','M0091',
        'M0101','M0111','M0121','M0131','M0141','M0151',
        'M0161','M0171'
    ) THEN 1 END) AS REALIZADO
FROM V_PBI_PECA_SERVICO P

WHERE P.EMPRESA = '1' 
AND P.REVENDA = '1'
AND P.ITEM IN (
    'M0041','M0051','M0061','M0071','M0081','M0091',
    'M0101','M0111','M0121','M0131','M0141','M0151',
    'M0161','M0171'
)
AND P.DATA >= TO_DATE('01/06/2026', 'DD/MM/YYYY')
AND P.DATA <= TO_DATE('01/07/2026', 'DD/MM/YYYY')
GROUP BY NOME_MECANICO


UNION ALL

SELECT 
    'TOTAL',
    COUNT(CASE WHEN P.ITEM = 'M0041' THEN 1 END),
    COUNT(CASE WHEN P.ITEM = 'M0051' THEN 1 END),
    COUNT(CASE WHEN P.ITEM = 'M0061' THEN 1 END),
    COUNT(CASE WHEN P.ITEM = 'M0071' THEN 1 END),
    COUNT(CASE WHEN P.ITEM = 'M0081' THEN 1 END),
    COUNT(CASE WHEN P.ITEM = 'M0091' THEN 1 END),
    COUNT(CASE WHEN P.ITEM = 'M0101' THEN 1 END),
    COUNT(CASE WHEN P.ITEM = 'M0111' THEN 1 END),
    COUNT(CASE WHEN P.ITEM = 'M0121' THEN 1 END),
    COUNT(CASE WHEN P.ITEM = 'M0131' THEN 1 END),
    COUNT(CASE WHEN P.ITEM = 'M0141' THEN 1 END),
    COUNT(CASE WHEN P.ITEM = 'M0151' THEN 1 END),
    COUNT(CASE WHEN P.ITEM = 'M0161' THEN 1 END),
    COUNT(CASE WHEN P.ITEM = 'M0171' THEN 1 END),
    COUNT(CASE WHEN P.ITEM IN (
        'M0041','M0051','M0061','M0071','M0081','M0091',
        'M0101','M0111','M0121','M0131','M0141','M0151',
        'M0161','M0171'
    ) THEN 1 END)
FROM V_PBI_PECA_SERVICO P

WHERE P.EMPRESA = '1' 
AND P.REVENDA = '1'
AND P.ITEM IN (
    'M0041','M0051','M0061','M0071','M0081','M0091',
    'M0101','M0111','M0121','M0131','M0141','M0151',
    'M0161','M0171'
)
AND P.DATA >= TO_DATE('01/06/2026', 'DD/MM/YYYY')
AND P.DATA <= TO_DATE('01/07/2026', 'DD/MM/YYYY')

ORDER BY NOME_MECANICO;